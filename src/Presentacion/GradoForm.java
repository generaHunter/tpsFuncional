    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        
        if (txtGrado.getText().length() > 0) {
            Grado gradoNew = new Grado();

            gradoNew.setGrado(txtGrado.getText());

            try {
                CGrado.create(gradoNew);
                
            } catch (Exception ex) {
                Logger.getLogger(GradoForm.class.getName()).log(Level.SEVERE, null, ex);
            }
            cargarGrado();
            txtGrado.setText("");
        }else {
        JOptionPane.showMessageDialog(null, "Falta ingresar el nombre del grado");
        }

    }//GEN-LAST:event_btnAgregarActionPerformed

    private void tableGradoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableGradoMouseClicked
        // TODO add your handling code here:
        
        int indice = this.tableGrado.getSelectedRow();
        
        if (indice > -1 ) {
            
            gradoEdit = (Grado) this.tableGrado.getValueAt(tableGrado.getSelectedRow(), 0);
            
            txtGrado.setText(tableGrado.getValueAt(indice, 2).toString());
        }
    }//GEN-LAST:event_tableGradoMouseClicked

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
        
       if (txtGrado.getText().length() > 0) {
            gradoEdit.setGrado(txtGrado.getText());
        
        try {
            CGrado.edit(gradoEdit);
        } catch (Exception ex) {
            Logger.getLogger(GradoForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        cargarGrado();
        txtGrado.setText("");
       }
           else {
        JOptionPane.showMessageDialog(null, "Falta ingresar el nombre del grado");
        }
   
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        try {
            // TODO add your handling code here:
            int opcion = JOptionPane.showConfirmDialog(null, "Est√° seguro que desea eliminar el Grado " + gradoEdit.getGrado(), "Eliminar Grado", JOptionPane.YES_NO_OPTION);
            if (opcion == 0) {

                CGrado.destroy(gradoEdit.getIdGrado());
            }
        } catch (NonexistentEntityException ex) {
            Logger.getLogger(GradoForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        cargarGrado();
    }//GEN-LAST:event_btnEliminarActionPerformed

