    private void cargarTurno(){
    
        try {
            modelTable.setRowCount(0);
            List<Turno> listTurno = CTurno.findTurnoEntities();
            Object o[] = null;
            for (int i = 0; i < listTurno.size(); i++) {
             
                modelTable.addRow(o);
                modelTable.setValueAt(listTurno.get(i), i, 0);
                modelTable.setValueAt(listTurno.get(i).getIdTurno(), i, 1);
                modelTable.setValueAt(listTurno.get(i).getTurno(), i, 2);
                
            }
            
        } catch (Exception e) {
        }
  
    
    }
    
