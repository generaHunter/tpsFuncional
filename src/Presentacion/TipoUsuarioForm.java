    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here
        
        
        if (txtTipo.getText().length() > 0) {
            TipoUsuario tipoNew = new TipoUsuario();

            tipoNew.setTipo(txtTipo.getText());
            try {
                CTipoU.create(tipoNew);
                cargarTipos();
                txtTipo.setText("");
            } catch (Exception ex) {
                Logger.getLogger(TipoUsuarioForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "El campo se encuentra vacio");
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void tableTipoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableTipoMouseClicked
        // TODO add your handling code here:
        
        int indice = tableTipo.getSelectedRow();
        
        if (indice > -1) {
         
             
             
             try {
                tipoEdit = (TipoUsuario) tableTipo.getValueAt(tableTipo.getSelectedRow(), 0);
                txtTipo.setText(modeloTabla.getValueAt(indice, 2).toString());
                
            } catch (Exception e) {
            }
        } 
       
        
        
    }//GEN-LAST:event_tableTipoMouseClicked

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
        
        if (txtTipo.getText().length() > 0) {
            tipoEdit.setTipo(txtTipo.getText());
            try {
                CTipoU.edit(tipoEdit);
            } catch (Exception ex) {
                Logger.getLogger(TipoUsuarioForm.class.getName()).log(Level.SEVERE, null, ex);
            }
            cargarTipos();
            txtTipo.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "El campo se encuentra vacio");
        }
        

    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        // TODO add your handling code here:
        
         int opcion = JOptionPane.showConfirmDialog(null, "Est√° seguro que desea eliminar el tipo "+tipoEdit.getTipo(), "Eliminar Tipo", JOptionPane.YES_NO_OPTION);
            if (opcion == 0) {
                try {
                    CTipoU.destroy(tipoEdit.getIdTipo());
                } catch (NonexistentEntityException ex) {
                    Logger.getLogger(EncargadoForm.class.getName()).log(Level.SEVERE, null, ex);
                }
                this.cargarTipos();
                LimpearCampos();
            }
    }//GEN-LAST:event_btnEliminarActionPerformed

    
    private void LimpearCampos(){
    txtTipo.setText("");
    }
