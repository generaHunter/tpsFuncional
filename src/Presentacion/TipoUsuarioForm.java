    private void cargarTipos(){
    
        modeloTabla.setRowCount(0);
        Object o[] = null;
        
        List<TipoUsuario> ListaTipo = CTipoU.findTipoUsuarioEntities();
        
        for (int i = 0; i < ListaTipo.size(); i++) {
            
            modeloTabla.addRow(o);
            modeloTabla.setValueAt(ListaTipo.get(i), i, 0);
            modeloTabla.setValueAt(ListaTipo.get(i).getIdTipo(), i, 1);
            modeloTabla.setValueAt(ListaTipo.get(i).getTipo(), i, 2);
            
        }
    
    }
    
